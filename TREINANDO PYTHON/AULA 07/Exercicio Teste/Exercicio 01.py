# Usando input para o usuário digitar um número
n1 = float(input("Digite algum valor: "))
n2 = float(input("Digite outro valor qualquer: "))

# Usando variaveis para colocar dentro da formatção
soma = n1+n2
multiplicacao = n1*n2
divisao = n1/n2
divisao_int = n1//n2
potencia = n1**n2

# Mostrando na tela os resultados da conta
print(f"A Soma vale: {soma}")
print(f"A Multiplicação vale: {multiplicacao}")
print(f"A Divisão vale: {divisao}")
print(f"A Divisão inteira vale: {divisao_int}")
print(f"A Potência vale: {potencia}")
